@bot.message_handler(content_types=['document'])
def handle_docs(message):
    user_id = message.from_user.id
    add_active_user(user_id)

    if bot_locked and user_id not in admin_ids:
        bot.reply_to(message, "🔒 The bot is currently locked by the admin. Only admins can use it right now.")
        return

    # Check file limit
    current_count = get_user_file_count(user_id)
    limit = get_user_file_limit(user_id)
    if current_count >= limit:
        bot.reply_to(message, f"❌ Limit reached! You have uploaded {current_count}/{limit} files. Delete old files to upload new ones.")
        return

    file_name = message.document.file_name
    if not (file_name.endswith('.py') or file_name.endswith('.js') or file_name.endswith('.zip')):
        bot.reply_to(message, "❌ Invalid file type! Only `.py`, `.js`, and `.zip` files are supported.")
        return

    try:
        # Download file to a safe variable structure
        file_info = bot.get_file(message.document.file_id)
        downloaded_file = bot.download_file(file_info.file_path)
        
        # If Admin or Owner uploads, save directly without restriction
        if user_id in admin_ids:
            user_folder = get_user_folder(user_id)
            file_path = os.path.join(user_folder, file_name)
            with open(file_path, 'wb') as f:
                f.write(downloaded_file)
            
            file_type = 'zip' if file_name.endswith('.zip') else ('js' if file_name.endswith('.js') else 'py')
            if file_type == 'zip':
                try:
                    with zipfile.ZipFile(file_path, 'r') as zip_ref:
                        zip_ref.extractall(user_folder)
                    os.remove(file_path)
                    bot.reply_to(message, "✅ ZIP file uploaded and extracted successfully!")
                    return
                except zipfile.BadZipFile:
                    bot.reply_to(message, "❌ Failed to extract. Bad ZIP file structure.")
                    if os.path.exists(file_path): os.remove(file_path)
                    return
            
            save_user_file(user_id, file_name, file_type)
            bot.reply_to(message, f"👑 Admin File `{file_name}` saved directly!")
            
            # Auto-run if python or js
            if file_type == 'py':
                threading.Thread(target=run_script, args=(file_path, user_id, user_folder, file_name, message)).start()
            elif file_type == 'js':
                threading.Thread(target=run_js_script, args=(file_path, user_id, user_folder, file_name, message)).start()
            return

        # --- 🛡️ For Regular Users: Admin Approval Pipeline ---
        request_id = f"req_{user_id}_{int(time.time())}"
        user_folder = get_user_folder(user_id)
        temp_file_path = os.path.join(user_folder, file_name)
        
        # Save file to disk temporarily pending approval
        with open(temp_file_path, 'wb') as f:
            f.write(downloaded_file)
            
        file_type = 'zip' if file_name.endswith('.zip') else ('js' if file_name.endswith('.js') else 'py')
        
        # Register request data in global memory tracker
        pending_approvals[request_id] = {
            'user_id': user_id,
            'file_name': file_name,
            'file_path': temp_file_path,
            'file_type': file_type,
            'chat_id': message.chat.id
        }
        
        bot.reply_to(message, "⏳ Your file has been received! Waiting for **ASIF BOSS** to approve it.")
        
        # Create Inline Buttons for Admin decision tree
        admin_markup = types.InlineKeyboardMarkup(row_width=2)
        approve_btn = types.InlineKeyboardButton("🟢 Approved", callback_data=f"approve_req_{request_id}")
        delete_btn = types.InlineKeyboardButton("🗑️ Delete System", callback_data=f"delete_req_{request_id}")
        admin_markup.add(approve_btn, delete_btn)
        
        request_msg = (
            f"🚨 **New File Permission Requested!**\n\n"
            f"👤 User ID: `{user_id}`\n"
            f"📄 File Name: `{file_name}`\n"
            f"⚙️ Type: `{file_type.upper()}`\n\n"
            f"ASIF BOSS, do you want to approve and run this script?"
        )
        
        # Alert the Owner/Admin directly
        bot.send_message(OWNER_ID, request_msg, parse_mode="Markdown", reply_markup=admin_markup)
        
    except Exception as e:
        logger.error(f"Error handling document upload: {e}", exc_info=True)
        bot.reply_to(message, f"❌ Error processing upload request: {e}")

